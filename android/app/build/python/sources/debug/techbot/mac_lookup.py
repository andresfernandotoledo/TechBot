def normalizar_mac(mac):
    mac = mac.strip().upper().replace("-", "").replace(":", "").replace(".", "")
    mac = re.sub(r"[^0-9A-F]", "", mac)
    return mac


def validar_mac(mac):
    m = normalizar_mac(mac)
    if len(m) != 12:
        return False
    try:
        int(m, 16)
        return True
    except ValueError:
        return False


def buscar_por_mac(mac):
    m = normalizar_mac(mac)
    if not validar_mac(m):
        return {"error": f"MAC inválida: {mac}. Debe tener 6 pares hexadecimales (ej: AA:BB:CC:DD:EE:FF)"}
    oui = m[:6]
    fabricante = OUI_DB.get(oui, "Desconocido / No asignado")
    return {
        "mac": mac.strip(),
        "mac_normalizada": m,
        "oui": oui,
        "fabricante": fabricante,
        "broadcast": m == "FFFFFFFFFFFF",
        "multicast": int(m[:2], 16) & 1 == 1,
        "virtual": "Virtual" in fabricante or "Virtual" in fabricante,
    }


def buscar_por_fabricante(query):
    q = query.strip().lower()
    resultados = []
    for oui, fabricante in OUI_DB.items():
        if q in fabricante.lower():
            resultados.append({"oui": oui, "fabricante": fabricante})
    resultados.sort(key=lambda x: x["fabricante"])
    return resultados


def listar_fabricantes():
    fabricantes = {}
    for oui, nombre in OUI_DB.items():
        if nombre not in fabricantes:
            fabricantes[nombre] = []
        fabricantes[nombre].append(oui)
    resultado = []
    for nombre, ouis in sorted(fabricantes.items()):
        resultado.append({"fabricante": nombre, "ouis": ouis, "cantidad": len(ouis)})
    return resultado
