def search_ports(query):
    query = query.lower()
    results = {}
    for port, service in COMMON_PORTS.items():
        if query in service.lower():
            results[port] = service
    return results

def get_port_range(start, end):
    results = {}
    for port in range(start, end + 1):
        service = COMMON_PORTS.get(port, "Desconocido")
        results[port] = service
    return results
